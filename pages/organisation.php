<h2>Comp&eacute;titions</h2>
<p class="text_paragraph">
Notre club participe durant toute la saison &agrave; plusieurs comp&eacute;titions. Celles-ci sont partag&eacute;es entre Championnat, Tournois de Classement, Championnat Cantonnal et Tournois Divers. Toutes les diff&eacute;rentes manifestations sont organis&eacute;es par l'AVVF.<br/><br/>

Quant au Championnat, il donne la possibilit&eacute; de jouer contre des &eacute;quipes venant d'autres clubs. Les rencontres se jouent, comme au foot, &agrave; l'aller et au retour, de fin ao&ucirc;t &agrave; fin juin.<br/>
Les matches ont toujours lieu &agrave; l'int&eacute;rieur et sont r&eacute;gis par des r&egrave;gles bien pr&eacute;cises: tables d'int&eacute;rieur r&eacute;glementaires, tenue r&eacute;glementaire (T-shirt de club et short), raquette r&eacute;glementaire (approuv&eacute;e par l'ITTF), balles officielles de comp&eacute;tition (3 &eacute;toiles, 40mm de diam&egrave;tre), etc... Les matches se jouent en 3 sets gagnants, de 11 points chacun. D'autres r&eacute;glementations sont &eacute;galement appliqu&eacute;es et peuvent &ecirc;tre consult&eacute;es dans la section appropri&eacute;e ou dir&eacute;ctement sur le site de l'AVVF.<br/>
<b>N'oubliez pas &eacute;galement que le fair-play joue un r&ocirc;le essentiel dans notre sport !</b><br/><br/>

Afin de pouvoir participer aux manifestations, une licence est requise. Seuls les joueurs licenci&eacute;s peuvent participer aux matches officiels ! <br/>
Des classements par rapport au niveau de jeu existent. Un joueur qui d&eacute;bute est class&eacute; D1 et le meilleur classement est A20. Les niveaux int&eacute;rmediares sont partag&eacute;s en cat&eacute;gories A, B, C et D, ou chaque cat&eacute;gorie est compos&eacute;e de 5 niveaux. Ainsi on aura les classements D1-D5, C6-C10, B11-B15 et A16-A20.<br/><br/>

Les championnat est compos&eacute; de ligues de diff&eacute;rents niveaux. Chaque &eacute;quipe d'un club est affect&eacute;e &agrave; une ligue et groupe particuliers. <br/>
On distingue 3 ligues nationales (LNA, LNB et LNC) et, dans le cas de l'AVVF, 7 ligues r&eacute;gionales.
