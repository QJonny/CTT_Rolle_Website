<p class="title">Equipes</p>

<p class="text_paragraph">Afin de pouvoir participer aux diff&eacute;rentes manifestations mises en place par l'AVVF, mais aussi dans le but de gagner de l'exp&eacute;rience personnelle, nos joueurs font partie de plusieurs &eacute;quipes disputant divers tournois r&eacute;gionaux.<br/><br/>
Actuellement, le CTT Rolle compte parmi ses rangs deux &eacute;quipes principales, <b>Rolle 1</b> et <b>Rolle 2</b>, ainsi qu'une &eacute;quipe <b>Jeunesse</b>.</p>

